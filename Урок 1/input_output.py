#сторока целиком
print('Привет, меня зовут Кеша мне 2 года')
#у нас есть 2 переменные
name = 'Кеша'
age = 2
# как вывести строку вместе с переменными в терминал
print(name, age)
# /  sep функция сепаратор
print(name, age, sep='/')
print(name, age, sep='/;h')
# end  перенос строки
print(name)
print(age)
print('------------->')
print(name, end=';')
print(age, end=';')